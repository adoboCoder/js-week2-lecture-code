// for loop statement that counts from 0-999
for (let i = 0; i < 1000; i++) {
    console.log(i);
}

// while loop statement that counts from 1000-1999
let inputNumber = 1000;

while (inputNumber < 2000) {
    console.log(inputNumber);
    inputNumber++;
}

// do-while loop statement (guaranteed to run at least once)
let inputNumber2 = 2000;
do {
    console.log(inputNumber2);
    inputNumber2++;
} while (inputNumber2 < 2000);
