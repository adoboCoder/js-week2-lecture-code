let myRating = 5; // 1 - highest, 3 - lowest

// switch statement that checks for rating
switch (myRating) {
    case 1:
        console.log("You get the highest rating!");
        break;
    case 2:
        console.log("You get an average rating!");
        break;
    case 3:
        console.log("You get the lowest rating!");
        break;
    default:
        console.log("Invalid rating!");
        break;
}